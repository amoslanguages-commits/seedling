                      _buildSectionHeader('Challenge Mode'),

                      const SizedBox(height: 16),
                      _buildModeSelection(),

                      const SizedBox(height: 32),
                      _buildSectionHeader('Select Theme'),
                      const SizedBox(height: 16),
                      _buildThemeSelector(),

