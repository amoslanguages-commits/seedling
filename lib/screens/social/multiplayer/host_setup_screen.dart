  Widget _buildModeSelection() {
    return Row(
      children: [
        Expanded(
          child: _buildGlassModeCard(
            title: 'Vocabulary',
            description: 'Blitz words',
            icon: '🌸',
            isSelected: _gameType == LiveGameType.vocabulary,
            onTap: () => setState(() => _gameType = LiveGameType.vocabulary),
          ),
        ),
        const SizedBox(width: 16),
        Expanded(
          child: _buildGlassModeCard(
            title: 'Sentences',
            description: 'Master syntax',
            icon: '🌳',
            isSelected: _gameType == LiveGameType.sentences,
            onTap: () => setState(() => _gameType = LiveGameType.sentences),
          ),
        ),
      ],
    );
  }

  Widget _buildGlassModeCard({
    required String title,
    required String description,
    required String icon,
    required bool isSelected,
    required VoidCallback onTap,
  }) {
    return GestureDetector(
      onTap: onTap,
      child: AnimatedContainer(
        duration: const Duration(milliseconds: 300),
        padding: const EdgeInsets.all(20),
        decoration: BoxDecoration(
          color: isSelected
              ? SeedlingColors.seedlingGreen.withValues(alpha: 0.2)
              : Colors.white.withValues(alpha: 0.03),
          borderRadius: BorderRadius.circular(24),
          border: Border.all(
            color: isSelected
                ? SeedlingColors.seedlingGreen.withValues(alpha: 0.5)
                : Colors.white.withValues(alpha: 0.1),
            width: isSelected ? 2 : 1,
          ),
          boxShadow: isSelected
              ? [
                  BoxShadow(
                    color: SeedlingColors.seedlingGreen.withValues(alpha: 0.2),
                    blurRadius: 15,
                    spreadRadius: 1,
                  ),
                ]
              : [],
        ),
        child: Column(
          children: [
            Text(icon, style: const TextStyle(fontSize: 40)),
            const SizedBox(height: 12),
            Text(
              title,
              style: SeedlingTypography.body.copyWith(
                color: Colors.white,
                fontWeight: FontWeight.bold,
              ),
            ),
            Text(
              description,
              style: SeedlingTypography.caption.copyWith(color: Colors.white60),
            ),
          ],
        ),
      ),
    );
  }

  Widget _buildThemeSelector() {
    final rootThemes = [
      'All',
      ...CategoryTaxonomy.getRootCategories().map((c) => c.name),
    ];

    return SingleChildScrollView(
      scrollDirection: Axis.horizontal,
      physics: const BouncingScrollPhysics(),
      child: Row(
        children: rootThemes.map((theme) {
          final isSelected = _selectedTheme == theme;
          return Padding(
            padding: const EdgeInsets.only(right: 12),
            child: GestureDetector(
              onTap: () => setState(() {
                _selectedTheme = theme;
                _selectedSubtheme = 'All'; // Reset subtheme
              }),
              child: AnimatedContainer(
                duration: const Duration(milliseconds: 200),
                padding: const EdgeInsets.symmetric(
                  horizontal: 20,
                  vertical: 12,
                ),
                decoration: BoxDecoration(
                  color: isSelected
                      ? SeedlingColors.autumnGold
                      : Colors.white.withValues(alpha: 0.05),
                  borderRadius: BorderRadius.circular(30),
                  border: Border.all(
                    color: isSelected ? Colors.white24 : Colors.white12,
                  ),
                ),
                child: Text(
                  theme,
                  style: SeedlingTypography.body.copyWith(
                    color: isSelected ? Colors.black87 : Colors.white,
                    fontWeight: isSelected
                        ? FontWeight.bold
                        : FontWeight.normal,
                  ),
                ),
              ),
            ),
          );
        }).toList(),
      ),
    );
  }

  Widget _buildSubthemeSelector() {
    // Find parent ID
    final parent = CategoryTaxonomy.getRootCategories().firstWhere(
      (c) => c.name == _selectedTheme,
      orElse: () => throw Exception('Theme not found'),
    );

    final subthemes = [
      'All',
      ...CategoryTaxonomy.getSubCategories(parent.id).map((c) => c.name),
    ];

    return SingleChildScrollView(
      scrollDirection: Axis.horizontal,
      physics: const BouncingScrollPhysics(),
      child: Row(
        children: subthemes.map((sub) {
          final isSelected = _selectedSubtheme == sub;
          return Padding(
            padding: const EdgeInsets.only(right: 12),
            child: GestureDetector(
              onTap: () => setState(() => _selectedSubtheme = sub),
              child: AnimatedContainer(
                duration: const Duration(milliseconds: 200),
                padding: const EdgeInsets.symmetric(
                  horizontal: 20,
                  vertical: 12,
                ),
                decoration: BoxDecoration(
                  color: isSelected
                      ? SeedlingColors.seedlingGreen
                      : Colors.white.withValues(alpha: 0.05),
                  borderRadius: BorderRadius.circular(30),
                  border: Border.all(
                    color: isSelected ? Colors.white24 : Colors.white12,
                  ),
                ),
                child: Text(
                  sub,
                  style: SeedlingTypography.body.copyWith(
                    color: isSelected ? Colors.black87 : Colors.white,
                    fontWeight: isSelected
                        ? FontWeight.bold
                        : FontWeight.normal,
                  ),
                ),
              ),
            ),
          );
        }).toList(),
      ),
    );
  }

  Widget _buildCourseSelector() {
    final courses = ref.watch(courseProvider);

    // Ensure we have a selection
    if (_selectedNativeCode == null && courses.courses.isNotEmpty) {
      final active = courses.courses.firstWhere(
        (c) => c.id == ref.read(courseProvider.notifier).activeCourseId,
        orElse: () => courses.courses.first,
      );
      _selectedNativeCode = active.nativeLanguage.code;
      _selectedTargetCode = active.targetLanguage.code;
    }

    return SingleChildScrollView(
      scrollDirection: Axis.horizontal,
      physics: const BouncingScrollPhysics(),
      child: Row(
        children: [
          ...courses.courses.map((course) {
            final isSelected =
                _selectedNativeCode == course.nativeLanguage.code &&
                _selectedTargetCode == course.targetLanguage.code;
            return Padding(
              padding: const EdgeInsets.only(right: 12),
              child: GestureDetector(
                onTap: () => setState(() {
                  _selectedNativeCode = course.nativeLanguage.code;
                  _selectedTargetCode = course.targetLanguage.code;
                }),
                child: AnimatedContainer(
                  duration: const Duration(milliseconds: 200),
                  padding: const EdgeInsets.all(16),
                  decoration: BoxDecoration(
                    color: isSelected
                        ? SeedlingColors.water.withValues(alpha: 0.2)
                        : Colors.white.withValues(alpha: 0.05),
                    borderRadius: BorderRadius.circular(24),
                    border: Border.all(
                      color: isSelected ? SeedlingColors.water : Colors.white12,
                    ),
                  ),
                  child: Row(
                    children: [
                      Twemoji(emoji: course.nativeLanguage.flag, height: 20),
                      const Padding(
                        padding: EdgeInsets.symmetric(horizontal: 8),
                        child: Icon(
                          Icons.arrow_forward_rounded,
                          color: Colors.white30,
                          size: 14,
                        ),
                      ),
                      Twemoji(emoji: course.targetLanguage.flag, height: 20),
                      const SizedBox(width: 8),
                      Text(
                        course.targetLanguage.name,
                        style: SeedlingTypography.caption.copyWith(
                          color: Colors.white,
                          fontWeight: isSelected
                              ? FontWeight.bold
                              : FontWeight.normal,
                        ),
                      ),
                    ],
                  ),
                ),
              ),
            );
          }),

          // Add Course Button
          GestureDetector(
            onTap: () {
              // Navigation to course management would go here
              ScaffoldMessenger.of(context).showSnackBar(
                const SnackBar(
                  content: Text('Manage your courses in the Vocabulary tab!'),
                ),
              );
            },
            child: Container(
              padding: const EdgeInsets.symmetric(horizontal: 20, vertical: 12),
              decoration: BoxDecoration(
                color: Colors.white.withValues(alpha: 0.05),
                borderRadius: BorderRadius.circular(30),
                border: Border.all(
                  color: Colors.white12,
                  style: BorderStyle.solid,
                ),
              ),
              child: const Row(
                children: [
                  Icon(
                    Icons.add_circle_outline_rounded,
                    color: SeedlingColors.freshSprout,
                    size: 20,
                  ),
                  SizedBox(width: 8),
                  Text('Add New', style: TextStyle(color: Colors.white)),
                ],
              ),
            ),
          ),
        ],
      ),
    );
  }

